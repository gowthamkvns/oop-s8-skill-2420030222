package skill1;

public class PrimitiveDataTypesDemo {
    public static void main(String[] args) {
        byte age = 25;
        short currentYear = 2025;
        int population = 1400000000;
        long distanceToMoon = 384400000L;
        float pi = 3.14f;
        double gravity = 9.80665;
        char grade = 'A';
        boolean isJavaFun = true;

        System.out.println("Primitive Data Types Demo:");
        System.out.println("---------------------------");
        System.out.println("Age: " + age);
        System.out.println("Current Year: " + currentYear);
        System.out.println("Population: " + population);
        System.out.println("Distance to Moon: " + distanceToMoon + " meters");
        System.out.println("Value of Pi: " + pi);
        System.out.println("Gravity: " + gravity + " m/sÂ²");
        System.out.println("Grade: " + grade);
        System.out.println("Is Java fun?: " + isJavaFun);
    }
}
